# Um jardineiro está plantando uma cerca viva que tem 25 metros de comprimento. Se ele
# precisa plantar uma muda a cada 50 centímetros, quantas mudas ele precisará?

cerca = 25
muda = 50 

mudas = (cerca*100) / muda

# (cerca*100) trasformei metros para centimetros depois dividi os espaçamentos das mudas 

print("o jardineiro precisará de",mudas,"mudas")


