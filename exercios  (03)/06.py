# Uma pessoa trabalha em uma empresa e recebe um salário base de R$ 2.500, mas também
# recebe um adicional de R$ 350 por mês por desempenho. Qual é o salário dessa pessoa em um ano?

salario = 2500
adicional = 350

salarioanual = (salario +  adicional)* 12

print(f"o salario anual dessa pessoa é {salarioanual}")