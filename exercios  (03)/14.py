# Um carro faz 12km por litro de gasolina. Se ele percorrer uma distância de 360km, quantos
# litros de gasolina ele gastará?

distancia1 = 360
distancia2 = 12
litro = 1

# 12km \ /  360km
#  1   / \  x = gasto

gasto = (distancia1 * litro)/ distancia2

print(f" O carro gastará {gasto} litros de gasolina")

