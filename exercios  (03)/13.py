# Uma loja de brinquedos está com uma promoção de 30% de desconto em um boneco que
#  custa R$ 60. Qual é o preço do boneco com desconto?

porcentagem = 30
precoinicial = 60 

#  60 \ / 100
#   x / \ 30

desconto = (porcentagem * precoinicial  ) / 100
#  valor do desconto do produto

valorfinal = precoinicial - desconto  

print(f"O preço do boneco com desconto {valorfinal} reais")




