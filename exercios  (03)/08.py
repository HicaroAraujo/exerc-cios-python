 # Um triângulo equilátero tem lado de medida 6cm. Calcule a sua área.
# Definindo o lado do triângulo equilátero.

lado = 6 

area = (lado**2  * (3**0.5)) / 4
    #  (raiz quadrada = 3 ** 0.5)  ----->>  formula para calcular a area triangulo equilatero = lado² * raiz de 3 dividido por 4 
print("A área do triângulo equilátero com lado de medida 6cm é:", area, "cm^2")
