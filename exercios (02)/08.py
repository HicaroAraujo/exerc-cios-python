# Faça um algoritmo que calcule a área de um retângulo de base e altura informados pelo usuário eimprima o resultado.

altura = float(input("digite a altura do retangulo:"))
base = float ( input("digite a base do retangulo:"))

# a area do retangulo e igual a base x altura 

area = base * altura 
print (f" a area do retangulo è {area}")