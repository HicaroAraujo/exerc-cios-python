# Faça um algoritmo que divida dois números e imprima o resultado.

numero1 = float (input("digite um numero:"))
numero2 = float (input("digite outro numero:"))

divisão = numero1 / numero2


print(f" a divisão dos numeros {numero1} e {numero2} é:{divisão:.2f}")

